"""금융업(은행/보험/금융지주 등) 여부를 원본 계정과목명으로 추정한다.

은행/보험/금융지주는 재무제표 구조 자체가 일반 기업과 다르다 — 유동/비유동
구분이 없는 유동성배열법 재무상태표를 쓰고, 매출액 대신 이자수익/보험수익 등을
핵심 손익으로 인식한다. 이 때문에 `ratios.py`의 표준 재무비율(특히 유동비율,
매출총이익률/영업이익률/순이익률)이 매핑 실패로 공란 처리되는데, 이는 계산
오류가 아니라 업종 특성이다. 사용자가 이를 "프로그램 고장"으로 오해하지
않도록 사전에 감지해 경고하기 위한 모듈이다.

주의: DART company.json의 induty_code(KSIC 업종코드)를 보조 신호로 검토했으나
채택하지 않았다 — 실제 조회 결과 순수 지주회사(예: SK 지주회사, 비금융, 표준
비율 8개 중 7개 정상 계산됨)와 금융지주회사(예: 우리금융지주, 비율 4개 공란
처리됨)가 동일한 induty_code(64992)를 반환해, 이 코드만으로는 두 경우를
구분할 수 없었다(KSIC 분류상 "지주회사" 자체가 금융업 대분류 코드에
속하기 때문). 따라서 계정과목명 감지만 사용한다.
"""

from __future__ import annotations

import pandas as pd

# 실제 DART 데이터로 검증됨(2026-07-18) — 삼성화재(보험)/우리금융지주(금융지주)/
# 기업은행(은행)/삼성증권(증권사) 4개 업종 실제 사업보고서에는 존재하고,
# SK지주(연결·별도)/SK하이닉스(연결·별도)/덴티움/인바이츠바이오코아/삼성전자
# (모두 일반 제조/서비스업)에는 전혀 존재하지 않음을 확인.
#   - 삼성화재: 보험계약부채/보험계약자산/보험수익/보험서비스비용/재보험계약부채/
#     재보험계약자산/재보험수익/이자수익/이자비용
#   - 우리금융지주: 위 보험 계열 전체 + 예수부채/이자수익/이자비용/투자계약부채
#   - 기업은행: 보험계약부채/보험수익/예수부채/이자수익/이자비용
#   - 삼성증권: 예수부채/이자수익/이자비용 (증권사는 보험 계열 마커가 없어도
#     이 3개만으로 정상 감지됨을 확인)
# "이자수익"/"이자비용"은 정확히 이 명칭일 때만 마커다 — 일반 기업은 동일한
# 현금흐름을 "이자의 수취"/"이자의 지급"으로 기재해 겹치지 않는다.
FINANCIAL_INDUSTRY_MARKERS: frozenset[str] = frozenset(
    {
        "보험계약부채",
        "보험계약자산",
        "보험수익",
        "보험서비스비용",
        "재보험계약부채",
        "재보험계약자산",
        "재보험수익",
        "재보험서비스비용",
        "예수부채",
        "이자수익",
        "이자비용",
        # [실데이터 미검증] IFRS17(2023년 시행) 도입 이전 구 명칭. 위 항목들과
        # 달리 실제 회사로 감지 여부를 확인하지 못했다 — 2025 사업연도 데이터에는
        # 등장하지 않았기 때문(IFRS17 시행 후 "보험계약부채" 등으로 대체됨).
        # 옛 사업연도(2022 이전) 사업보고서를 조회할 경우를 대비해 포함해두되,
        # 실제로 그런 케이스를 만나면 반드시 재검증할 것.
        "책임준비금",
        "투자계약부채",
    }
)


def detect_financial_industry(long_df: pd.DataFrame) -> list[str]:
    """원본 계정과목명(account_nm) 중 금융업 특유 계정과 일치하는 항목을 반환한다.

    반환 목록이 비어 있지 않으면 해당 회사를 은행/보험/금융지주 등
    금융업으로 추정한다 (임의 추정이 아니라 실제 공시된 계정과목명 기반).
    """
    if long_df.empty or "account_nm" not in long_df.columns:
        return []
    present = set(long_df["account_nm"].unique())
    return sorted(present & FINANCIAL_INDUSTRY_MARKERS)
